"""
SMART LEAD HUNTER — Comprehensive Hotel Brand Registrylitan hotel": "unknown",
=======================================================
Maps every hotel brand to its parent company, operating model,
procurement structure, and decision-maker profile.

This is the single source of truth for brand intelligence.
When a new hotel lead is discovered, this registry tells us:
  - Who actually makes uniform purchasing decisions
  - Whether a GPO (like Avendra) controls vendor selection
  - Which contacts to prioritize for outreach
  - How much autonomy the property GM has

OPERATING MODELS:
  managed        = Brand/operator runs the hotel for the owner (brand controls procurement)
  franchised     = Owner/mgmt company runs it, just uses the brand name (more freedom)
  collection     = Soft brand — independent hotel affiliated with a collection (most freedom)
  independent    = No brand affiliation (total owner/developer control)
  all_inclusive  = Corporate procurement dominates, property GM has minimal authority

PROCUREMENT MODELS:
  avendra_gpo    = Uses Avendra GPO — approved vendor list, JA must be on it to sell easily
  brand_managed  = Brand corporate procurement team controls OS&E (similar to Avendra)
  owner_managed  = Owner/management company controls procurement (no GPO constraint)
  fully_open     = Independent — owner/developer makes all decisions, best opportunity

CONTACT PRIORITY (pre-opening):
  1 = Chain/brand VP Operations or COO — controls pre-opening budget before GM hired
  2 = Opening/Pre-Opening GM — if appointed (typically 6-12mo before opening)
  3 = Director of Housekeeping — uniform specifics once hired
  4 = Purchasing Manager — once property is closer to opening

USAGE:
    from app.config.brand_registry import BrandRegistry
    info = BrandRegistry.lookup("Canopy by Hilton")
    # → BrandInfo(parent="Hilton", tier="tier3_upper_upscale", ...)
"""

from dataclasses import dataclass
from typing import Optional


@dataclass
class BrandInfo:
    """Complete intelligence profile for a hotel brand."""

    # Identity
    parent_company: str  # e.g. "Marriott", "Hilton", "Independent"
    tier: str  # tier1_ultra_luxury / tier2_luxury / tier3_upper_upscale / tier4_upscale / unknown

    # Operating model
    operating_model: (
        str  # managed / franchised / collection / independent / all_inclusive
    )
    typical_model_note: str  # Human-readable explanation

    # Procurement
    procurement_model: str  # avendra_gpo / brand_managed / owner_managed / fully_open
    gpo: Optional[str]  # "Avendra" / None
    uniform_freedom: str  # high / medium / low
    procurement_note: str  # How uniform decisions actually get made

    # Pre-opening contact strategy
    pre_opening_decision_maker: str  # Who to target BEFORE property GM is hired
    pre_opening_contact_titles: list  # Specific titles to search for
    opening_contact_titles: list  # Titles once hotel is 3-6mo from opening

    # Scoring adjustment
    contact_score_multiplier: float  # Multiplier on contact scores (1.0 = no change)
    opportunity_level: str  # high / medium / low


